package dto;

/**
 *
 * @author Javier
 */
public class RecursoDTO {
    private String nombre;
    private String tipo;
    private double tarifa;

    public RecursoDTO(String nombre, String tipo, double tarifa) {
        this.nombre = nombre;
        this.tipo = tipo;
        this.tarifa = tarifa;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public double getTarifa() {
        return tarifa;
    }

    public void setTarifa(double tarifa) {
        this.tarifa = tarifa;
    }
}
